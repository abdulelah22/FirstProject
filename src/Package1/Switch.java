package Package1;

public class Switch {
    public static void main(String[] args) {

        String j = "Two";

        switch(j){
            case "zero":
                System.out.print("value is 0");
                break;
            case"one":
                System.out.print("value is 1");
                break;
            case"Two":
                System.out.print("value is 2");
                break;
        }
    }
}